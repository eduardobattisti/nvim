      local git_branch = git_dashboard[#git_dashboard]

      if git_repo == nil and git_branch == nil then
        return {
          type = 'text',
          val = '',
          opts = { position = 'center' },
        }
      end

      local git_branch_section = {
        type = 'text',
        val = ' ' .. git_repo .. ':' .. git_branch,
        opts = { position = 'center' },
      }

      return git_branch_section
